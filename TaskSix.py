a = float(input("Результат в первый день в км :"))
b = float(input("Достижение результата в км :"))
days = 1
km = a
while km <= b:
    a = a + a * 0.1
    days += 1
    km = a
    print("результат во %d день: %.2f км." % (days, km))
    if km >= b:
        print(f"Спортсмен достигнет требуемых показателей на %.d день" % days)
        break
    else:
        print("Спортсмен пока не достиг желаемого результата.")
